const source = [1, 2, false, 3, 'use', 4, 4, false, 5, 6, 1, 7];
const set = []

for (let i = 0; i < source.length; i++) {
    let isDuplicate = false;
    for (let j = 0; j < set.length; j++) {
        if (source[i] === set[j]) {
            isDuplicate = true;
            break;
        }
    }
    if (!isDuplicate) {
        set.push(source[i]);
    }
}
